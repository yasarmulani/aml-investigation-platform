          <div style={{ fontSize: 13, fontWeight: 600, color: detail.sar_recommended ? '#166534' : '#b91c1c' }}>
            {detail.sar_recommended ? 'SAR filing recommended' : 'Insufficient indicators for SAR'}
          </div>
